def reconstruct_and_visualize(App, Gui, filepath: str, max_wafers: int = None):
    """Parse a cut list file and reconstruct all segments in the active doc.

    Reconstruction follows 'Wafer Reconstruction.txt': each wafer is built
    independently in its local frame and assembled by mark-alignment.

    If the original segment is found in the document, the reconstruction
    is auto-aligned to overlay it (50% transparent orange/amber).  Otherwise
    the reconstruction is placed +200mm in X.

    Parameters
    ----------
    max_wafers : int or None
        Limit reconstruction to the first N wafers.  When set, debug plane
        markers are also added at the first wafer's entry/exit planes.
    """
    doc = App.activeDocument()
    if doc is None:
        doc = App.newDocument("Reconstruction")

    segments = parse_cut_list(filepath)
    if not segments:
        raise ValueError(f"No segments found in: {filepath}")

    for seg_data in segments:
        alignment = _find_original_alignment(doc, seg_data['name'])
        if alignment is None:
            logger.info("Segment '%s': original not found — +200mm X fallback",
                        seg_data['name'])

        wafers = reconstruct_segment(seg_data, max_wafers=max_wafers)

        rec_part = visualize_reconstruction(
            doc, seg_data['name'], wafers, alignment)

        if max_wafers is not None and max_wafers <= 5:
            radius = seg_data['cylinder_diameter'] / 2.0
            orig_placement = _get_part_placement(doc, f"{seg_data['name']}_Part")
            rec_placement  = (rec_part.Placement if rec_part is not None
                              else App.Placement())
            _add_debug_planes(doc, seg_data['name'], radius,
                              orig_placement, rec_placement)

        compare_reconstruction(doc, seg_data['name'], seg_data.get('rows'),
                               rec_wafers=wafers, alignment=alignment)

    if Gui is not None:
        try:
            Gui.SendMsgToActiveView("ViewFit")
        except Exception:
            pass

    doc.recompute()
    logger.info("Reconstruction complete: %d segment(s) from %s",
                len(segments), filepath)
