def _perp_to(v: App.Vector) -> App.Vector:
    """Return an arbitrary unit vector perpendicular to v."""
    v = App.Vector(v)
    v.normalize()
    candidate = App.Vector(1.0, 0.0, 0.0)
    if abs(v.dot(candidate)) > 0.9:
        candidate = App.Vector(0.0, 1.0, 0.0)
    result = v.cross(candidate)
    result.normalize()
    return result


def _build_rotation_from_frame(v1_l: App.Vector, v2_l: App.Vector,
                                v1_w: App.Vector, v2_w: App.Vector) -> App.Rotation:
    """Build the rotation R that maps v1_l → v1_w and v2_l → v2_w.

    v1_l and v2_l must be orthogonal unit vectors (local frame pair).
    v1_w and v2_w must be orthogonal unit vectors (world frame pair).
    The third axis is the right-hand cross product: v3 = v1 × v2.

    The rotation matrix elements satisfy:
        R[i][j] = sum_k  v{k}_w[i] * v{k}_l[j]
    """
    v3_l = v1_l.cross(v2_l)
    v3_w = v1_w.cross(v2_w)

    m = App.Matrix()
    m.A11 = v1_w.x * v1_l.x + v2_w.x * v2_l.x + v3_w.x * v3_l.x
    m.A12 = v1_w.x * v1_l.y + v2_w.x * v2_l.y + v3_w.x * v3_l.y
    m.A13 = v1_w.x * v1_l.z + v2_w.x * v2_l.z + v3_w.x * v3_l.z
    m.A21 = v1_w.y * v1_l.x + v2_w.y * v2_l.x + v3_w.y * v3_l.x
    m.A22 = v1_w.y * v1_l.y + v2_w.y * v2_l.y + v3_w.y * v3_l.y
    m.A23 = v1_w.y * v1_l.z + v2_w.y * v2_l.z + v3_w.y * v3_l.z
    m.A31 = v1_w.z * v1_l.x + v2_w.z * v2_l.x + v3_w.z * v3_l.x
    m.A32 = v1_w.z * v1_l.y + v2_w.z * v2_l.y + v3_w.z * v3_l.y
    m.A33 = v1_w.z * v1_l.z + v2_w.z * v2_l.z + v3_w.z * v3_l.z
    m.A44 = 1.0
    return App.Placement(m).Rotation


def _build_wafer_local(theta_entry_deg: float, theta_exit_deg: float,
                       rot_deg: float, L: float, radius: float):
    """Build a wafer solid in the wafer's local coordinate frame.

    Local frame convention (from 'Wafer Reconstruction.txt'):
        Cylinder axis = Z, entry face center at (0,0,0), exit at (0,0,L).
        Major-axis direction M = X = (1,0,0).
        M' = M rotated by rot_deg around Z.

    Cutting planes (spec outward normals):
        n_entry = −cos(θ_entry)·Z − sin(θ_entry)·M
        n_exit  =  cos(θ_exit)·Z  − sin(θ_exit)·M'

    The solid is a straight cylinder (axis Z) intersected with the two
    half-spaces.

    Returns (solid, plane1, plane2) where plane normals are stored as
    INWARD directions (T_entry, T_exit ≈ +Z) for compatibility with
    compare_reconstruction.  Returns (None, None, None) on failure.
    """
    te = math.radians(theta_entry_deg)
    tx = math.radians(theta_exit_deg)
    rot_rad = math.radians(rot_deg)

    # Spec outward normals
    n_entry_spec = App.Vector(-math.sin(te), 0.0, -math.cos(te))
    n_exit_spec  = App.Vector(
        -math.sin(tx) * math.cos(rot_rad),
        -math.sin(tx) * math.sin(rot_rad),
         math.cos(tx))

    # Cylinder must extend beyond both cutting planes
    tan_e = abs(math.tan(te)) if abs(te) < math.radians(89.9) else 1e6
    tan_x = abs(math.tan(tx)) if abs(tx) < math.radians(89.9) else 1e6
    extra_entry = radius * tan_e + radius
    extra_exit  = radius * tan_x + radius

    z_start    = -extra_entry
    cyl_height = L + extra_entry + extra_exit
    plane_size = radius * 100.0

    try:
        cylinder = Part.makeCylinder(
            radius, cyl_height,
            App.Vector(0.0, 0.0, z_start),
            App.Vector(0.0, 0.0, 1.0))

        # Cut away entry side: half-space on n_entry_spec side of z=0 plane
        c1 = Part.Circle(App.Vector(0, 0, 0), n_entry_spec, plane_size)
        f1 = Part.Face(Part.Wire([c1.toShape()]))
        hs1 = f1.extrude(n_entry_spec * plane_size)
        wafer = cylinder.cut(hs1)

        # Cut away exit side: half-space on n_exit_spec side of z=L plane
        c2 = Part.Circle(App.Vector(0, 0, L), n_exit_spec, plane_size)
        f2 = Part.Face(Part.Wire([c2.toShape()]))
        hs2 = f2.extrude(n_exit_spec * plane_size)
        wafer = wafer.cut(hs2)

        if wafer.ShapeType == 'Compound' and len(wafer.Solids) > 0:
            wafer = wafer.Solids[0]

        if not hasattr(wafer, 'Volume') or wafer.Volume < 1e-6:
            return None, None, None

    except Exception as exc:
        logger.error("_build_wafer_local error: %s", exc)
        return None, None, None

    # Inward normals (direction of travel) for Wafer plane dicts
    T_entry_l = App.Vector( math.sin(te), 0.0,  math.cos(te))
    T_exit_l  = App.Vector(
        -math.sin(tx) * math.cos(rot_rad),
        -math.sin(tx) * math.sin(rot_rad),
         math.cos(tx))

    plane1 = {'point': App.Vector(0.0, 0.0, 0.0), 'normal': T_entry_l}
    plane2 = {'point': App.Vector(0.0, 0.0, L),   'normal': T_exit_l}

    return wafer, plane1, plane2


