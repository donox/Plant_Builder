def reconstruct_segment(segment_data: dict, max_wafers=None) -> list:
    """Reconstruct a list of Wafer objects from a parsed segment dict.

    Implements the mark-alignment assembly rule from 'Wafer Reconstruction.txt':
    each wafer is built in its own local frame and placed so that the entry
    mark coincides with the preceding wafer's exit mark.

    The placement of each wafer is determined by two orthonormal pairs:
        v1_l = n_entry_spec  (outward entry normal in local, ≈ −Z)
        v2_l = mark_dir_l    (direction entry center → entry mark in local)
    mapped to world targets:
        v1_w = −F_prev       (anti-parallel to preceding exit normal → face aligned)
        v2_w = mark_dir_prev (direction from preceding exit center to exit mark)

    All plane1/plane2 positions and normals are stored in the reconstruction's
    local frame (origin at the first wafer entry center, initial direction +Z).
    """
    radius = segment_data['cylinder_diameter'] / 2.0
    rows   = segment_data['rows']
    if max_wafers is not None:
        rows = rows[:max_wafers]
        logger.info("reconstruct_segment: limiting to first %d wafer(s)", max_wafers)

    wafers = []

    # ── Initial joint state (implicit flat-circle cut 0 for open curve) ──────
    # Starting position: origin.  Direction of travel: +Z.
    # Initial M direction: +X (arbitrary; determines global orientation).
    # Exit "mark" of the implicit cut 0 = origin + radius * M_dir = (R, 0, 0).
    joint_pos   = App.Vector(0.0, 0.0, 0.0)
    F_world     = App.Vector(0.0, 0.0, 1.0)   # outward exit normal of preceding cut
    mk_exit     = App.Vector(radius, 0.0, 0.0) # preceding exit mark position

    theta_entry_deg = 0.0   # blade angle of entry cut (0 = implicit flat circle)

    for i, row in enumerate(rows):
        theta_exit_deg = row['blade_deg']
        rot_deg        = row['rot_deg']
        L              = row['length']

        logger.info("── Wafer %d (cut #%d): entry_blade°=%.2f  exit_blade°=%.2f  "
                    "rot°=%.2f  len=%.4f",
                    i, row['cut'], theta_entry_deg, theta_exit_deg, rot_deg, L)

        # ── Build wafer solid in local frame ──────────────────────────────────
        solid_local, p1_l, p2_l = _build_wafer_local(
            theta_entry_deg, theta_exit_deg, rot_deg, L, radius)

        # ── Compute placement (mark-alignment assembly rule) ──────────────────
        te = math.radians(theta_entry_deg)

        # Local entry frame vectors (orthogonal pair, both unit):
        #   v1_l = spec outward entry normal = −cos(θ)·Z − sin(θ)·M
        #   v2_l = direction from entry center to entry mark (unit)
        #        = cos(θ)·M − sin(θ)·Z  = (cos(θ), 0, −sin(θ))
        v1_l = App.Vector(-math.sin(te), 0.0, -math.cos(te))
        v2_l = App.Vector( math.cos(te), 0.0, -math.sin(te))

        # World targets:
        #   v1_l must map to −F_world (face alignment)
        #   v2_l must map to normalize(mk_exit − joint_pos) (mark alignment)
        v1_w = App.Vector(-F_world.x, -F_world.y, -F_world.z)

        mk_dir = mk_exit - joint_pos
        if mk_dir.Length < 1e-9:
            mk_dir = _perp_to(F_world)
            logger.warning("  Wafer %d: degenerate mark direction — using fallback", i)
        else:
            mk_dir.normalize()
        v2_w = mk_dir

        rotation  = _build_rotation_from_frame(v1_l, v2_l, v1_w, v2_w)
        placement = App.Placement(joint_pos, rotation)

        logger.info("  Placement: origin=(%.4f,%.4f,%.4f)  "
                    "Z_world=(%.4f,%.4f,%.4f)  X_world=(%.4f,%.4f,%.4f)",
                    joint_pos.x, joint_pos.y, joint_pos.z,
                    rotation.multVec(App.Vector(0, 0, 1)).x,
                    rotation.multVec(App.Vector(0, 0, 1)).y,
                    rotation.multVec(App.Vector(0, 0, 1)).z,
                    rotation.multVec(App.Vector(1, 0, 0)).x,
                    rotation.multVec(App.Vector(1, 0, 0)).y,
                    rotation.multVec(App.Vector(1, 0, 0)).z)

        # ── Transform solid and planes to reconstruction local frame ──────────
        if solid_local is not None:
            solid_rec = solid_local.copy()
            solid_rec.transformShape(placement.toMatrix())

            p1_point = placement.multVec(p1_l['point'])
            p2_point = placement.multVec(p2_l['point'])
            p1_norm  = rotation.multVec(p1_l['normal'])
            p2_norm  = rotation.multVec(p2_l['normal'])

            plane1_rec = {'point': p1_point, 'normal': p1_norm}
            plane2_rec = {'point': p2_point, 'normal': p2_norm}

            geom = {
                'lift_angle_deg':    2.0 * theta_exit_deg,
                'rotation_angle_deg': rot_deg,
                'rotation_angle_rad': math.radians(rot_deg),
            }
            wafers.append(Wafer(solid_rec, i, plane1_rec, plane2_rec, geom))
            logger.info("  Wafer %d OK: volume=%.4f", i, solid_rec.Volume)
        else:
            logger.warning("  Wafer %d: degenerate geometry — solid skipped", i)

        # ── Advance joint state to exit of this wafer ─────────────────────────
        tx      = math.radians(theta_exit_deg)
        rot_rad = math.radians(rot_deg)

        # Exit center in reconstruction local frame
        joint_pos = placement.multVec(App.Vector(0.0, 0.0, L))

        # Exit face outward normal in local: n_exit = cos(θ)·Z − sin(θ)·M'
        n_exit_l = App.Vector(
            -math.sin(tx) * math.cos(rot_rad),
            -math.sin(tx) * math.sin(rot_rad),
             math.cos(tx))
        F_world = rotation.multVec(n_exit_l)

        # Exit mark in local: (R·cos(Rot°), R·sin(Rot°), L + R·tan(θ_exit))
        tan_x = math.tan(tx) if abs(tx) < math.radians(89.9) else 1e6
        mk_exit_l = App.Vector(
            radius * math.cos(rot_rad),
            radius * math.sin(rot_rad),
            L + radius * tan_x)
        mk_exit = placement.multVec(mk_exit_l)

        logger.info("  Joint: pos=(%.4f,%.4f,%.4f)  "
                    "F=(%.4f,%.4f,%.4f)",
                    joint_pos.x, joint_pos.y, joint_pos.z,
                    F_world.x, F_world.y, F_world.z)

        # Entry angle for next wafer = exit angle of this wafer
        theta_entry_deg = theta_exit_deg

    logger.info("Reconstructed %d wafers for segment '%s'",
                len(wafers), segment_data['name'])
    return wafers


