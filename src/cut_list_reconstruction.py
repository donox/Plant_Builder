"""cut_list_reconstruction.py - Build a 3D model from a cut list text file.

Parses a cutting list generated by the PlantBuilder driver and reconstructs
the 3D wafer geometry following 'Wafer Reconstruction.txt'.

Each wafer is built independently in its own local coordinate frame (cylinder
axis = Z, major-axis M = X) and then assembled by the mark-alignment rule:
  1. Face alignment  — orient entry face toward the exit face of the preceding wafer
  2. Center alignment — entry center coincides with the preceding exit center
  3. Mark alignment  — rotate about the shared face normal until marks coincide

No global coordinate system, frame chaining, or Cylinder° column is needed.

Entry point: reconstruct_and_visualize(App, Gui, filepath, max_wafers=None)
"""

import math

import FreeCAD as App
import Part

from wafer_loft import Wafer
from core.logging_setup import get_logger

logger = get_logger(__name__)


# ---------------------------------------------------------------------------
# Fractional inch parser
# ---------------------------------------------------------------------------

def _parse_fractional_inches(s: str) -> float:
    """Parse '1 3/8"' → 1.375, '3/4"' → 0.75, '2"' → 2.0, '0"' → 0.0."""
    s = s.strip().rstrip('"').strip()
    if not s:
        return 0.0
    parts = s.split()
    if not parts:
        return 0.0
    if '/' in parts[0]:
        num, den = parts[0].split('/')
        return int(num) / int(den)
    whole = int(parts[0])
    if len(parts) > 1 and '/' in parts[1]:
        num, den = parts[1].split('/')
        return whole + int(num) / int(den)
    return float(whole)


