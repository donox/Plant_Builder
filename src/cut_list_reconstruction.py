# Cut list parser
# ---------------------------------------------------------------------------

def parse_cut_list(filepath: str) -> list:
    """Parse a PlantBuilder cut list text file.

    Returns a list of segment dicts, one per SEGMENT: block::

        {
          'name': 'circle',
          'cylinder_diameter': 2.0,
          'initial_offset': 15.3,      # Cylinder° initial offset (not used for reconstruction)
          'rows': [
            {'cut': 1, 'length': 1.375, 'blade_deg': 2.5,
             'rot_deg': 0.0, 'cylinder_deg': 15.3},
            ...
          ]
        }
    """
    segments = []
    current_seg = None
    in_wafer_settings = False
    in_data_rows = False

    with open(filepath, 'r') as f:
        lines = f.readlines()

    for raw_line in lines:
        line = raw_line.rstrip('\n')
        stripped = line.strip()

        # ── New SEGMENT block ───────────────────────────────────────────
        if stripped.startswith('SEGMENT:'):
            if current_seg is not None:
                segments.append(current_seg)
            seg_name = stripped[len('SEGMENT:'):].strip()
            current_seg = {
                'name': seg_name,
                'cylinder_diameter': 2.0,
                'initial_offset': 0.0,
                'curve_type': '',
                'closed': True,          # default: closed curve
                'initial_blade_deg': None,  # entry blade of wafer 0 (closed only)
                'sigma_blade_deg': None,    # build variance: 1σ blade tilt
                'sigma_rot_deg': None,      # build variance: 1σ cylinder rotation
                '_has_mm_col': False,       # whether mm column is present in data rows
                '_has_entry_exit_blade': False,  # whether EntryBlade°/ExitBlade° columns present
                'rows': [],
            }
            in_wafer_settings = False
            in_data_rows = False
            continue

        if current_seg is None:
            continue

        # ── Curve type (heuristic open/closed from type name) ───────────
        if stripped.startswith('Curve type:'):
            ct = stripped.split(':', 1)[1].strip().lower()
            current_seg['curve_type'] = ct
            # Known explicitly-open curve types; everything else defaults closed.
            # Overridden if 'Curve closed:' line is also present.
            _OPEN_TYPES = {
                'bezier', 'bspline', 'b_spline', 'sinusoidal', 'linear',
                'line', 'open', 'open_curve', 'helical_arc',
            }
            current_seg['closed'] = ct not in _OPEN_TYPES
            continue

        # ── Curve closed (explicit, takes priority over type heuristic) ─
        if stripped.startswith('Curve closed:'):
            val = stripped.split(':', 1)[1].strip().lower()
            current_seg['closed'] = val in ('true', 'yes', '1')
            continue

        # ── Initial blade (entry blade of wafer 0, closed curves only) ─
        if stripped.startswith('Initial blade:'):
            try:
                val = stripped.split(':', 1)[1].strip().rstrip('°').strip()
                current_seg['initial_blade_deg'] = float(val)
            except (ValueError, IndexError):
                pass
            continue

        # ── Build variance header lines ─────────────────────────────────
        if 'Blade' in stripped and 'variance' in stripped and '±' in stripped:
            try:
                val = stripped.split('±', 1)[1].rstrip('°').strip()
                current_seg['sigma_blade_deg'] = float(val)
            except (ValueError, IndexError):
                pass
            continue
        if 'Rot' in stripped and 'variance' in stripped and '±' in stripped:
            try:
                val = stripped.split('±', 1)[1].rstrip('°').strip()
                current_seg['sigma_rot_deg'] = float(val)
            except (ValueError, IndexError):
                pass
            continue

        # ── Wafer settings block ────────────────────────────────────────
        if stripped == 'Wafer settings:':
            in_wafer_settings = True
            continue

        if in_wafer_settings:
            if stripped == '' or (line and not line[0].isspace()):
                in_wafer_settings = False
                # fall through so this line can be processed further
            else:
                if stripped.startswith('cylinder_diameter:'):
                    try:
                        val = stripped.split(':', 1)[1].strip()
                        current_seg['cylinder_diameter'] = float(val)
                    except (ValueError, IndexError):
                        pass
                continue

        # ── Initial offset ──────────────────────────────────────────────
        if 'Cylinder\u00b0 initial offset:' in stripped:
            try:
                val = stripped.split(':', 1)[1].strip().rstrip('\u00b0')
                current_seg['initial_offset'] = float(val)
            except (ValueError, IndexError):
                pass
            continue

        # ── Header row → start collecting data ─────────────────────────
        if (stripped.startswith('Cut') and 'Length' in stripped
                and 'Blade' in stripped):
            current_seg['_has_mm_col'] = 'mm' in stripped.split()
            current_seg['_has_entry_exit_blade'] = 'EntryBlade' in stripped
            in_data_rows = True
            continue

        # ── End of data table ───────────────────────────────────────────
        if in_data_rows and stripped.startswith('Total cylinder length'):
            in_data_rows = False
            continue

        # ── All-dash separator ──────────────────────────────────────────
        if stripped and all(c == '-' for c in stripped):
            continue

        if not in_data_rows:
            continue

        # ── Parse data row ──────────────────────────────────────────────
        tokens = stripped.split()
        if not tokens:
            continue
        try:
            cut_num = int(tokens[0])
        except ValueError:
            continue

        # Length may span two tokens for mixed numbers like "1 3/8""
        idx = 1
        length_parts = []
        while idx < len(tokens):
            length_parts.append(tokens[idx])
            idx += 1
            if length_parts[-1].endswith('"'):
                break

        if not length_parts:
            logger.warning("Row %d: no length token found, skipping", cut_num)
            continue

        length_str = ' '.join(length_parts)

        # Optional mm column: appears right after the fractional length.
        # When present, use the mm value as the authoritative length (higher precision).
        has_mm = current_seg.get('_has_mm_col', False)
        if has_mm and idx < len(tokens):
            try:
                mm_val = float(tokens[idx])
                length = mm_val / 25.4
                idx += 1
            except ValueError:
                length = _parse_fractional_inches(length_str)
        else:
            length = _parse_fractional_inches(length_str)

        if idx >= len(tokens):
            logger.warning("Row %d: not enough tokens after length, skipping", cut_num)
            continue

        try:
            blade_deg = float(tokens[idx])
            idx += 1
        except ValueError:
            logger.warning("Row %d: could not parse blade_deg, skipping", cut_num)
            continue

        # Optional EntryBlade° and ExitBlade° columns
        has_entry_exit = current_seg.get('_has_entry_exit_blade', False)
        theta_entry_deg = None
        theta_exit_deg  = None
        if has_entry_exit and idx + 1 < len(tokens):
            try:
                theta_entry_deg = float(tokens[idx])
                theta_exit_deg  = float(tokens[idx + 1])
                idx += 2
            except ValueError:
                pass

        if idx + 1 >= len(tokens):
            logger.warning("Row %d: not enough tokens for rot/cylinder, skipping", cut_num)
            continue

        try:
            rot_deg      = float(tokens[idx])
            cylinder_deg = float(tokens[idx + 1])
        except ValueError:
            logger.warning("Row %d: could not parse rot/cylinder columns, skipping", cut_num)
            continue

        row_dict = {
            'cut':          cut_num,
            'length':       length,
            'blade_deg':    blade_deg,
            'rot_deg':      rot_deg,
            'cylinder_deg': cylinder_deg,
        }
        if theta_entry_deg is not None:
            row_dict['theta_entry_deg'] = theta_entry_deg
        if theta_exit_deg is not None:
            row_dict['theta_exit_deg'] = theta_exit_deg
        current_seg['rows'].append(row_dict)

    if current_seg is not None:
        segments.append(current_seg)

    logger.info("Parsed %d segment(s) from %s", len(segments), filepath)
    for seg in segments:
        ibd = seg.get('initial_blade_deg')
        ibd_str = f"{ibd:.3f}°" if ibd is not None else "not set"
        logger.info("  Segment '%s': %d rows, diameter=%.3f, curve_type='%s', "
                    "closed=%s, initial_blade=%s",
                    seg['name'], len(seg['rows']), seg['cylinder_diameter'],
                    seg.get('curve_type', '?'), seg.get('closed', True), ibd_str)

    return segments


