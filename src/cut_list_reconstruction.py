def _find_planar_faces(shape) -> list:
    """Return the planar faces of a wafer solid, sorted by area (largest first)."""
    planar = [face for face in shape.Faces
              if isinstance(face.Surface, Part.Plane)]
    planar.sort(key=lambda f: f.Area, reverse=True)
    return planar


def _face_normal(face) -> App.Vector:
    """Return the face's outward normal at the parameter-space midpoint."""
    try:
        pr = face.ParameterRange
        u = (pr[0] + pr[1]) * 0.5
        v = (pr[2] + pr[3]) * 0.5
        return face.normalAt(u, v)
    except Exception:
        return face.normalAt(0, 0)


def _find_original_alignment(doc, seg_name: str):
    """Compute a Placement that maps the reconstruction's local frame onto
    the original segment's first wafer entry face in the document.

    Returns App.Placement on success, or None if the original is not found.

    Strategy (priority order):
    1. LCS objects (LCS_{seg}_0_1): exact position and full rotation.
    2. Adjacent-wafer adjacency: exit face of wafer 0 is shared with wafer 1.
    3. Dot-product fallback when wafer 1 is absent.
    """
    candidates = doc.getObjectsByLabel(f"Wafer_{seg_name}_0")
    if not candidates:
        return None

    first_wafer_obj = candidates[0]
    shape = getattr(first_wafer_obj, 'Shape', None)
    if shape is None or shape.isNull():
        return None

    part_placement = App.Placement()
    part_candidates = doc.getObjectsByLabel(f"{seg_name}_Part")
    if part_candidates and hasattr(part_candidates[0], 'Placement'):
        part_placement = part_candidates[0].Placement

    # ── Priority 1: LCS (exact, full rotation) ───────────────────────────────
    lcs_candidates = doc.getObjectsByLabel(f"LCS_{seg_name}_0_1")
    if lcs_candidates and hasattr(lcs_candidates[0], 'Placement'):
        lcs_pl = lcs_candidates[0].Placement
        t_start = lcs_pl.Rotation.multVec(App.Vector(0, 0, 1))
        logger.info("Auto-align '%s' (LCS): entry=%s  T_start=%s",
                    seg_name, lcs_pl.Base, t_start)
        return lcs_pl

    # ── Fallback: face detection ──────────────────────────────────────────────
    planar = _find_planar_faces(shape)
    if len(planar) < 2:
        logger.warning("Could not find 2 planar faces in 'Wafer_%s_0'; "
                       "skipping auto-align", seg_name)
        return None

    fa, fb = planar[0], planar[1]
    c_a, c_b = fa.CenterOfMass, fb.CenterOfMass

    entry_face = None
    wafer1_cands = doc.getObjectsByLabel(f"Wafer_{seg_name}_1")
    if wafer1_cands:
        shape1 = getattr(wafer1_cands[0], 'Shape', None)
        if shape1 and not shape1.isNull():
            planar1 = _find_planar_faces(shape1)
            if planar1:
                w1_centers = [f.CenterOfMass for f in planar1]
                dist_a = min((c_a - c1).Length for c1 in w1_centers)
                dist_b = min((c_b - c1).Length for c1 in w1_centers)
                exit_face  = fa if dist_a < dist_b else fb
                entry_face = fb if dist_a < dist_b else fa
                logger.info("Auto-align '%s': exit↔wafer1 dist_a=%.4f dist_b=%.4f",
                            seg_name, dist_a, dist_b)

    if entry_face is None:
        chord_local = c_b - c_a
        if chord_local.Length < 1e-9:
            return None
        chord_local.normalize()
        n_a = _face_normal(fa)
        entry_face = fa if n_a.dot(chord_local) < 0 else fb
        logger.info("Auto-align '%s': using dot-product fallback", seg_name)

    local_center = entry_face.CenterOfMass
    local_n_out  = _face_normal(entry_face)
    local_n_out.normalize()

    world_center = part_placement.multVec(local_center)
    world_n_out  = part_placement.Rotation.multVec(local_n_out)

    t_start = App.Vector(-world_n_out.x, -world_n_out.y, -world_n_out.z)
    t_start.normalize()

    z_axis = App.Vector(0, 0, 1)
    dot = max(-1.0, min(1.0, t_start.dot(z_axis)))
    if dot > 1.0 - 1e-9:
        rotation = App.Rotation()
    elif dot < -1.0 + 1e-9:
        rotation = App.Rotation(App.Vector(1, 0, 0), 180.0)
    else:
        rotation = App.Rotation(z_axis, t_start)

    placement = App.Placement(world_center, rotation)
    logger.info("Auto-align '%s' (faces): entry=%s  T_start=%s",
                seg_name, world_center, t_start)
    return placement


