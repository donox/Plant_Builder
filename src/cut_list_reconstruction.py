def visualize_reconstruction(doc, seg_name: str, wafers: list,
                             placement=None):
    """Create FreeCAD visualization of reconstructed wafers.

    Tree structure::

        {seg_name}_Reconstructed    (App::Part)
        └── {seg_name}_Rec_Wafers   (App::DocumentObjectGroup)
            ├── Wafer_{seg_name}_Rec_0  (Part::Feature)  ← orange
            ├── Wafer_{seg_name}_Rec_1                   ← amber
            └── ...

    placement: if the original is found, the Part is placed to overlay it
               (50% transparent); otherwise offset +200mm in X.
    """
    part_name  = f"{seg_name}_Reconstructed"
    group_name = f"{seg_name}_Rec_Wafers"

    seg_part = doc.addObject("App::Part", part_name)
    aligned = placement is not None
    seg_part.Placement = (placement if aligned
                          else App.Placement(App.Vector(200, 0, 0),
                                            App.Rotation()))

    wafer_group = doc.addObject("App::DocumentObjectGroup", group_name)
    transparency = 50 if aligned else 0

    wafer_count = 0
    for wafer_obj in wafers:
        if wafer_obj.wafer is None:
            continue

        obj_name = f"Wafer_{seg_name}_Rec_{wafer_obj.index}"
        feat = doc.addObject("Part::Feature", obj_name)
        feat.Shape = wafer_obj.wafer

        if wafer_obj.index % 2 == 0:
            feat.ViewObject.ShapeColor = (1.0, 0.6, 0.2)
        else:
            feat.ViewObject.ShapeColor = (1.0, 0.85, 0.4)

        feat.ViewObject.Transparency = transparency
        wafer_group.addObject(feat)
        wafer_count += 1

    seg_part.addObject(wafer_group)
    doc.recompute()

    if aligned:
        logger.info("Visualization: %d wafers in '%s' (aligned, 50%% transparent)",
                    wafer_count, part_name)
    else:
        logger.info("Visualization: %d wafers in '%s' (+200mm X fallback)",
                    wafer_count, part_name)
    return seg_part


