def _add_debug_planes(doc, seg_name: str, cylinder_radius: float,
                      orig_placement, rec_placement):
    """Add thin-disc markers at entry/exit planes of the first wafer pair.

    4 discs: bright red = orig entry, dark red = orig exit,
             bright green = rec entry, dark green = rec exit.
    """
    disc_radius = cylinder_radius * 1.5
    disc_height = 0.05

    def _extract_entry_exit(shape, placement):
        planar = _find_planar_faces(shape)
        if len(planar) < 2:
            return None
        fa, fb = planar[0], planar[1]
        chord_local = fb.CenterOfMass - fa.CenterOfMass
        if chord_local.Length < 1e-9:
            return None
        chord_local.normalize()
        n_a = _face_normal(fa)
        entry_face = fa if n_a.dot(chord_local) < 0 else fb
        exit_face  = fb if n_a.dot(chord_local) < 0 else fa

        entry_c = placement.multVec(entry_face.CenterOfMass)
        entry_n = placement.Rotation.multVec(_face_normal(entry_face))
        entry_n.normalize()
        exit_c  = placement.multVec(exit_face.CenterOfMass)
        exit_n  = placement.Rotation.multVec(_face_normal(exit_face))
        exit_n.normalize()
        return entry_c, entry_n, exit_c, exit_n

    def _make_disc(center_world, normal_world, color, name):
        half_h = disc_height * 0.5
        base = App.Vector(center_world.x - normal_world.x * half_h,
                          center_world.y - normal_world.y * half_h,
                          center_world.z - normal_world.z * half_h)
        try:
            disc = Part.makeCylinder(disc_radius, disc_height, base, normal_world)
        except Exception as exc:
            logger.warning("_make_disc '%s': %s", name, exc)
            return None
        feat = doc.addObject("Part::Feature", name)
        feat.Shape = disc
        feat.ViewObject.ShapeColor = color
        feat.ViewObject.Transparency = 20
        return feat

    group = doc.addObject("App::DocumentObjectGroup", f"{seg_name}_DebugPlanes")

    orig_objs = doc.getObjectsByLabel(f"Wafer_{seg_name}_0")
    if orig_objs:
        shp = getattr(orig_objs[0], 'Shape', None)
        if shp and not shp.isNull():
            result = _extract_entry_exit(shp, orig_placement)
            if result:
                e_c, e_n, x_c, x_n = result
                f1 = _make_disc(e_c, e_n, (1.0, 0.2, 0.2), f"{seg_name}_Orig_Entry")
                f2 = _make_disc(x_c, x_n, (0.6, 0.0, 0.0), f"{seg_name}_Orig_Exit")
                if f1: group.addObject(f1)
                if f2: group.addObject(f2)
                logger.info("Orig wafer 0 entry: center=%s  n=%s", e_c, e_n)
                logger.info("Orig wafer 0 exit:  center=%s  n=%s", x_c, x_n)

    rec_objs = doc.getObjectsByLabel(f"Wafer_{seg_name}_Rec_0")
    if rec_objs:
        shp = getattr(rec_objs[0], 'Shape', None)
        if shp and not shp.isNull():
            result = _extract_entry_exit(shp, rec_placement)
            if result:
                e_c, e_n, x_c, x_n = result
                f1 = _make_disc(e_c, e_n, (0.2, 1.0, 0.2), f"{seg_name}_Rec_Entry")
                f2 = _make_disc(x_c, x_n, (0.0, 0.6, 0.0), f"{seg_name}_Rec_Exit")
                if f1: group.addObject(f1)
                if f2: group.addObject(f2)
                logger.info("Rec  wafer 0 entry: center=%s  n=%s", e_c, e_n)
                logger.info("Rec  wafer 0 exit:  center=%s  n=%s", x_c, x_n)

    doc.recompute()
    logger.info("Debug planes added to '%s_DebugPlanes'", seg_name)


