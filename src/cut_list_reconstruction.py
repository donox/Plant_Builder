"""cut_list_reconstruction.py - Build a 3D model from a cut list text file.

Parses a cutting list generated by the PlantBuilder driver and reconstructs
the 3D wafer geometry following 'Wafer Reconstruction.txt'.

Each wafer is built independently in its own local coordinate frame (cylinder
axis = Z, major-axis M = X) and then assembled by the mark-alignment rule:
  1. Face alignment  — orient entry face toward the exit face of the preceding wafer
  2. Center alignment — entry center coincides with the preceding exit center
  3. Mark alignment  — rotate about the shared face normal until marks coincide

No global coordinate system, frame chaining, or Cylinder° column is needed.

Entry point: reconstruct_and_visualize(App, Gui, filepath, max_wafers=None)
"""

import math

import FreeCAD as App
import Part

from wafer_loft import Wafer
from core.logging_setup import get_logger

logger = get_logger(__name__)


# ---------------------------------------------------------------------------
# Fractional inch parser
# ---------------------------------------------------------------------------

def _parse_fractional_inches(s: str) -> float:
    """Parse '1 3/8"' → 1.375, '3/4"' → 0.75, '2"' → 2.0, '0"' → 0.0."""
    s = s.strip().rstrip('"').strip()
    if not s:
        return 0.0
    parts = s.split()
    if not parts:
        return 0.0
    if '/' in parts[0]:
        num, den = parts[0].split('/')
        return int(num) / int(den)
    whole = int(parts[0])
    if len(parts) > 1 and '/' in parts[1]:
        num, den = parts[1].split('/')
        return whole + int(num) / int(den)
    return float(whole)


# ---------------------------------------------------------------------------
# Cut list parser
# ---------------------------------------------------------------------------

def parse_cut_list(filepath: str) -> list:
    """Parse a PlantBuilder cut list text file.

    Returns a list of segment dicts, one per SEGMENT: block::

        {
          'name': 'circle',
          'cylinder_diameter': 2.0,
          'initial_offset': 15.3,      # Cylinder° initial offset (not used for reconstruction)
          'rows': [
            {'cut': 1, 'length': 1.375, 'blade_deg': 2.5,
             'rot_deg': 0.0, 'cylinder_deg': 15.3},
            ...
          ]
        }
    """
    segments = []
    current_seg = None
    in_wafer_settings = False
    in_data_rows = False

    with open(filepath, 'r') as f:
        lines = f.readlines()

    for raw_line in lines:
        line = raw_line.rstrip('\n')
        stripped = line.strip()

        # ── New SEGMENT block ───────────────────────────────────────────
        if stripped.startswith('SEGMENT:'):
            if current_seg is not None:
                segments.append(current_seg)
            seg_name = stripped[len('SEGMENT:'):].strip()
            current_seg = {
                'name': seg_name,
                'cylinder_diameter': 2.0,
                'initial_offset': 0.0,
                'rows': [],
            }
            in_wafer_settings = False
            in_data_rows = False
            continue

        if current_seg is None:
            continue

        # ── Wafer settings block ────────────────────────────────────────
        if stripped == 'Wafer settings:':
            in_wafer_settings = True
            continue

        if in_wafer_settings:
            if stripped == '' or (line and not line[0].isspace()):
                in_wafer_settings = False
                # fall through so this line can be processed further
            else:
                if stripped.startswith('cylinder_diameter:'):
                    try:
                        val = stripped.split(':', 1)[1].strip()
                        current_seg['cylinder_diameter'] = float(val)
                    except (ValueError, IndexError):
                        pass
                continue

        # ── Initial offset ──────────────────────────────────────────────
        if 'Cylinder\u00b0 initial offset:' in stripped:
            try:
                val = stripped.split(':', 1)[1].strip().rstrip('\u00b0')
                current_seg['initial_offset'] = float(val)
            except (ValueError, IndexError):
                pass
            continue

        # ── Header row → start collecting data ─────────────────────────
        if (stripped.startswith('Cut') and 'Length' in stripped
                and 'Blade' in stripped):
            in_data_rows = True
            continue

        # ── End of data table ───────────────────────────────────────────
        if in_data_rows and stripped.startswith('Total cylinder length'):
            in_data_rows = False
            continue

        # ── All-dash separator ──────────────────────────────────────────
        if stripped and all(c == '-' for c in stripped):
            continue

        if not in_data_rows:
            continue

        # ── Parse data row ──────────────────────────────────────────────
        tokens = stripped.split()
        if not tokens:
            continue
        try:
            cut_num = int(tokens[0])
        except ValueError:
            continue

        # Length may span two tokens for mixed numbers like "1 3/8""
        idx = 1
        length_parts = []
        while idx < len(tokens):
            length_parts.append(tokens[idx])
            idx += 1
            if length_parts[-1].endswith('"'):
                break

        if not length_parts:
            logger.warning("Row %d: no length token found, skipping", cut_num)
            continue

        length_str = ' '.join(length_parts)
        length = _parse_fractional_inches(length_str)

        if idx + 2 >= len(tokens):
            logger.warning("Row %d: not enough tokens after length, skipping", cut_num)
            continue

        try:
            blade_deg    = float(tokens[idx])
            rot_deg      = float(tokens[idx + 1])
            cylinder_deg = float(tokens[idx + 2])
        except ValueError:
            logger.warning("Row %d: could not parse numeric columns, skipping", cut_num)
            continue

        current_seg['rows'].append({
            'cut':          cut_num,
            'length':       length,
            'blade_deg':    blade_deg,
            'rot_deg':      rot_deg,
            'cylinder_deg': cylinder_deg,
        })

    if current_seg is not None:
        segments.append(current_seg)

    logger.info("Parsed %d segment(s) from %s", len(segments), filepath)
    for seg in segments:
        logger.info("  Segment '%s': %d rows, diameter=%.3f",
                    seg['name'], len(seg['rows']), seg['cylinder_diameter'])

    return segments


