def _get_part_placement(doc, part_label: str):
    """Return the Placement of a Part object, or identity if not found."""
    candidates = doc.getObjectsByLabel(part_label)
    if candidates and hasattr(candidates[0], 'Placement'):
        return candidates[0].Placement
    return App.Placement()


def _extract_wafer_params(shape, part_placement):
    """Extract blade°, chord_length, and world-space entry centre from a shape."""
    planar = _find_planar_faces(shape)
    if len(planar) < 2:
        return None

    face_a, face_b = planar[0], planar[1]
    c_a = face_a.CenterOfMass
    c_b = face_b.CenterOfMass
    chord_local = c_b - c_a
    if chord_local.Length < 1e-9:
        return None
    chord_norm = App.Vector(chord_local)
    chord_norm.normalize()

    n_a = _face_normal(face_a)
    if n_a.dot(chord_norm) < 0:
        entry_face, exit_face = face_a, face_b
    else:
        entry_face, exit_face = face_b, face_a

    t_entry = App.Vector(-_face_normal(entry_face).x,
                         -_face_normal(entry_face).y,
                         -_face_normal(entry_face).z)
    t_exit  = _face_normal(exit_face)
    dot = max(-1.0, min(1.0, t_entry.dot(t_exit)))
    lift_angle_deg = math.degrees(math.acos(dot))

    entry_world = part_placement.multVec(entry_face.CenterOfMass)

    return {
        'blade_deg':    lift_angle_deg / 2.0,
        'chord_length': chord_local.Length,
        'entry_world':  entry_world,
    }


def compare_reconstruction(doc, seg_name: str, rows: list = None,
                           rec_wafers=None, alignment=None):
    """Log a per-wafer comparison table between original build and reconstruction.

    Columns: Cut, Rot°, Bld°CL, Bld°Or, Bld°Re, Len CL, Len Or, Len Re,
             PosErr, Δx, Δy, Δz.

    rec_wafers: Wafer list from reconstruct_segment (plane1/plane2 in
                reconstruction local frame).
    alignment:  Placement mapping reconstruction local → world.
    """
    orig_placement = _get_part_placement(doc, f"{seg_name}_Part")
    rec_wafer_dict = {w.index: w for w in rec_wafers} if rec_wafers else {}
    align = alignment if alignment is not None else App.Placement()

    hdr = (f"{'Cut':>4}  {'Rot°':>7}  "
           f"{'Bld°CL':>7}  {'Bld°Or':>7}  {'Bld°Re':>7}  "
           f"{'Len CL':>7}  {'Len Or':>7}  {'Len Re':>7}  "
           f"{'PosErr':>7}  {'Δx':>8}  {'Δy':>8}  {'Δz':>8}")
    logger.info("=== Wafer comparison: %s ===", seg_name)
    logger.info(hdr)
    logger.info("-" * len(hdr))

    i = 0
    while True:
        orig_objs = doc.getObjectsByLabel(f"Wafer_{seg_name}_{i}")
        rec_objs  = doc.getObjectsByLabel(f"Wafer_{seg_name}_Rec_{i}")
        has_rec   = bool(rec_objs) or (i in rec_wafer_dict)

        if not orig_objs and not has_rec:
            break

        cl_row = rows[i] if (rows and i < len(rows)) else None

        orig_params = None
        if orig_objs:
            shp = getattr(orig_objs[0], 'Shape', None)
            if shp and not shp.isNull():
                orig_params = _extract_wafer_params(shp, orig_placement)

        if orig_params is not None:
            lcs_cands = doc.getObjectsByLabel(f"LCS_{seg_name}_{i}_1")
            if lcs_cands and hasattr(lcs_cands[0], 'Placement'):
                orig_params['entry_world'] = lcs_cands[0].Placement.Base

        rec_params = None
        if i in rec_wafer_dict:
            w = rec_wafer_dict[i]
            T_entry = w.plane1['normal']
            T_exit  = w.plane2['normal']
            dot = max(-1.0, min(1.0, T_entry.dot(T_exit)))
            lift_angle_deg = math.degrees(math.acos(dot))
            chord_len = (w.plane2['point'] - w.plane1['point']).Length
            entry_world = align.multVec(w.plane1['point'])
            rec_params = {
                'blade_deg':    lift_angle_deg / 2.0,
                'chord_length': chord_len,
                'entry_world':  entry_world,
            }
        elif rec_objs:
            rec_placement = _get_part_placement(doc, f"{seg_name}_Reconstructed")
            shp = getattr(rec_objs[0], 'Shape', None)
            if shp and not shp.isNull():
                rec_params = _extract_wafer_params(shp, rec_placement)

        pos_err = None
        dx = dy = dz = None
        if orig_params and rec_params:
            diff = orig_params['entry_world'] - rec_params['entry_world']
            pos_err = diff.Length
            dx, dy, dz = diff.x, diff.y, diff.z

        rot_cl  = f"{cl_row['rot_deg']:+7.1f}"  if cl_row       else "      -"
        bld_cl  = f"{cl_row['blade_deg']:7.2f}"  if cl_row       else "      -"
        len_cl  = f"{cl_row['length']:7.4f}"     if cl_row       else "      -"
        bld_or  = f"{orig_params['blade_deg']:7.2f}"    if orig_params else "      -"
        bld_re  = f"{rec_params['blade_deg']:7.2f}"     if rec_params  else "      -"
        len_or  = f"{orig_params['chord_length']:7.4f}" if orig_params else "      -"
        len_re  = f"{rec_params['chord_length']:7.4f}"  if rec_params  else "      -"
        pos_str = f"{pos_err:7.4f}"              if pos_err is not None else "      -"
        dx_str  = f"{dx:+8.4f}"                  if dx is not None else "       -"
        dy_str  = f"{dy:+8.4f}"                  if dy is not None else "       -"
        dz_str  = f"{dz:+8.4f}"                  if dz is not None else "       -"

        logger.info("%4d  %s  %s  %s  %s  %s  %s  %s  %s  %s  %s  %s",
                    i + 1,
                    rot_cl, bld_cl, bld_or, bld_re,
                    len_cl, len_or, len_re,
                    pos_str, dx_str, dy_str, dz_str)
        i += 1

    logger.info("--- Total wafers compared: %d ---", i)


